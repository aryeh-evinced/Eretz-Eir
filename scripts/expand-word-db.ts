function main(): void {
  const db = new Database(DB_PATH);
  db.pragma("journal_mode = WAL");

  // Ensure schema exists
  db.exec(`
    CREATE TABLE IF NOT EXISTS words (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      letter TEXT NOT NULL,
      category TEXT NOT NULL,
      word TEXT NOT NULL,
      normalized TEXT NOT NULL,
      source TEXT NOT NULL DEFAULT 'seed',
      created_at TEXT NOT NULL DEFAULT (datetime('now')),
      UNIQUE(letter, category, normalized)
    );
    CREATE INDEX IF NOT EXISTS idx_words_lookup ON words(letter, category, normalized);
    CREATE INDEX IF NOT EXISTS idx_words_letter ON words(letter);
  `);

  const stmt = db.prepare(
    "INSERT OR IGNORE INTO words (letter, category, word, normalized, source) VALUES (?, ?, ?, ?, ?)",
  );

  const beforeCount = (db.prepare("SELECT COUNT(*) as c FROM words").get() as { c: number }).c;

  let inserted = 0;
  let attempted = 0;

  function insertData(data: WordData, source: string): void {
    const tx = db.transaction(() => {
      for (const [letter, categories] of Object.entries(data)) {
        for (const [category, words] of Object.entries(categories)) {
          for (const word of words) {
            attempted++;
            const normalized = sanitize(word);
            if (!normalized) continue;
            // Check word starts with the correct letter (or one of its alternates)
            if (!normalized.startsWith(letter) && !(letter === "כ" && normalized.startsWith("ק"))) continue;
            const result = stmt.run(letter, category, word.trim(), normalized, source);
            if (result.changes > 0) inserted++;
          }
        }
      }
    });
    tx();
  }

  insertData(getGameSiteData(), "scraped");
  insertData(getCuratedExpansions(), "curated-v2");

  const afterCount = (db.prepare("SELECT COUNT(*) as c FROM words").get() as { c: number }).c;

  console.log(`Expansion complete: ${inserted} new words inserted (${attempted} attempted).`);
  console.log(`DB grew from ${beforeCount} → ${afterCount} words.\n`);

  // Print per-category stats
  const stats = db
    .prepare("SELECT category, COUNT(*) as c FROM words GROUP BY category ORDER BY c DESC")
    .all() as { category: string; c: number }[];
  console.log("Per-category totals:");
  for (const s of stats) {
    console.log(`  ${s.category}: ${s.c}`);
  }

  // Print per-letter stats
  console.log("\nPer-letter totals:");
  const letterStats = db
    .prepare("SELECT letter, COUNT(*) as c FROM words GROUP BY letter ORDER BY letter")
    .all() as { letter: string; c: number }[];
  for (const s of letterStats) {
    console.log(`  ${s.letter}: ${s.c}`);
  }

  db.close();
}

main();
