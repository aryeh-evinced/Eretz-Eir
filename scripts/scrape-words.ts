function sanitize(text: string): string {
  return text
    .slice(0, 30)
    .replace(/[\n\r\t\u0000-\u001F\u007F-\u009F]/g, "")
    .replace(/[{}[\]]/g, "")
    .replace(/[\u0591-\u05C7]/g, "")
    .replace(/['"״׳]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

